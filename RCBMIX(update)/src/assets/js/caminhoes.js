function formatValue(val) {
    const formattedValue = val.toFixed(2);
    const [reais, cents] = formattedValue.split('.');

    const reaisParts = [];
    let index = reais.length;

    while (index > 0) {
        reaisParts.unshift(reais.slice(Math.max(index - 3, 0), index));
        index -= 3;
    }

    return reaisParts.join('.') + ',' + cents;
}

function calcularSomaTotal() {
    // Inicialize as somas com zero
    let somaFaturado = 0;
    let somaGasto = 0;
    let somaLucro = 0;
    let pneusViagem = 0;
    let gastoCombustivel = 0;
    let gastoMotorista = 0;

    // Itere sobre as empresas e some os valores
    for (const placaCaminhao in caminhoesValues[0]) {
        const caminhao = caminhoesValues[0][placaCaminhao];
        somaFaturado += caminhao.faturado;
        somaGasto += caminhao.gastoMotorista + caminhao.gastoViagem;
        somaLucro += caminhao.lucro;
        pneusViagem += caminhao.gastoPneus;
        gastoCombustivel += caminhao.combustivel;
        gastoMotorista += caminhao.gastoMotorista;
    }

    return {
        somaFaturado: formatValue(somaFaturado),
        somaGasto: formatValue(somaGasto),
        somaLucro: formatValue(somaLucro),
        pneusViagem: formatValue(pneusViagem),
        gastoCombustivel: formatValue(gastoCombustivel),
        gastoMotorista: formatValue(gastoMotorista)
    };
}

function updateValues(selectCaminhao) {
    if (selectCaminhao === 'Default') {
        let somaTotal = calcularSomaTotal();
        faturado.textContent = 'R$' + somaTotal.somaFaturado;
        gasto.textContent = 'R$' + somaTotal.somaGasto;
        lucro.textContent = 'R$' + somaTotal.somaLucro;
        pneus.textContent = 'R$' + somaTotal.pneusViagem;
        combustivel.textContent = 'R$' + somaTotal.gastoCombustivel;
        motorista.textContent = 'R$' + somaTotal.gastoMotorista;
    } else {
        const selectedEmpresa = caminhoesValues[0][selectCaminhao];

        if (selectedEmpresa) {
            faturado.textContent = 'R$' + formatValue(selectedEmpresa.faturado);
            gasto.textContent = 'R$' + formatValue(selectedEmpresa.gastoMotorista);
            lucro.textContent = 'R$' + formatValue(selectedEmpresa.lucro);
            pneus.textContent = 'R$' + formatValue(selectedEmpresa.gastoPneus)
            combustivel.textContent = 'R$' + formatValue(selectedEmpresa.combustivel);
            motorista.textContent = 'R$' + formatValue(selectedEmpresa.gastoMotorista)
        }
    }
}
